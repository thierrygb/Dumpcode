package Exercicio08;

public class Cargo {
	private String titulo;
	private double salario;
	
	public void setTitulo(String titulo){
		this.titulo=titulo;
	}
	public String getTitulo(){
		return titulo;
	}
	public void setSalario(double salario){
		this.salario=salario;
	}
	public double getSalario(){
		return salario;
	}
	
}