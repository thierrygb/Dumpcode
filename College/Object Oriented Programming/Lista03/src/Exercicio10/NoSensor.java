package Exercicio10;

public class NoSensor {
	public static int x;
	public static int y;
	public static String id;
	
	public NoSensor() {
	x=0;
	y=0;
	id= "";
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
}